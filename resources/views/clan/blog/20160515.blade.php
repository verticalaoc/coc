                <strong id="STRONG_16">全新友誼戰機制！</strong>
            </p>
            <p id="P_17">
                透過舉行友誼戰，部落成員們可以相互進攻進行練習、比賽或者只是簡單地打著玩！友誼戰沒有次數限制，且不會消耗雙方任何金幣、部隊、法術、英雄或陷阱。您也無法從中取得任何資源、獎盃或獎勵。
            </p>
            <p id="P_18">
                <br id="BR_19" /><br id="BR_20" /><strong id="STRONG_21">發起友誼戰：</strong>
            </p>
            <p id="P_22">
                部落內部聊天介面頂端新增了“友誼戰”（Challenge)圖示，點擊它，您便可以邀請部落成員來進攻您的村莊。您可以選擇當前村莊陣型或已保存的其他有效陣型（也包括部落對戰陣型）來進行防禦。可以在邀請中寫上您想說的話（也可以不寫），然後將您的友誼戰邀請發到聊天視窗即可。非常簡單！
            </p>
            <p id="P_23">
                友誼戰邀請發出後，您部落中的任一成員都可以接受挑戰並向您已選定的村莊陣型發動進攻。不用擔心，您不會因此丟失任何資源或獎盃。如果發出友誼戰邀請時您的部落城堡中已有援兵，那麼他們也會參與防守，而您的援兵也並不會因友誼戰而受到任何損失。
            </p>
            <p id="P_24">
                <br id="BR_25" /><br id="BR_26" /><strong id="STRONG_27">接受友誼戰：</strong>
            </p>
            <p id="P_28">
                接受友誼戰邀請後，您可以使用現有的軍隊對其村莊發動進攻。請放心，您在戰鬥中的所有消耗都將會在友誼戰結束後返還給您，所以您可以盡情大戰一場！
            </p>
            <p id="P_29">
                所有部落成員都可以在部落聊天介面查看友誼戰重播和結果。當然，如果部落成員們迫不及待想觀看你們的對戰，他們可以選擇即時觀看友誼戰。
            </p>
            <p id="P_30">
                <br id="BR_31" /><br id="BR_32" /><strong id="STRONG_33">在練習與嘗試中提升！</strong>
            </p>
            <p id="P_34">
                透過友誼戰您可以很好地提升戰鬥技巧、完善防禦陣型、試用不同兵種，或者只是跟部落成員玩一玩。希望您可以藉此探索出多樣的兵種搭配、新的防禦陣型甚至是進行一些瘋狂的嘗試！部落成員之間也可以藉此組織比賽或者只是簡單的攻防對決。
            </p>
            <p id="P_35">
                我們希望看到首領們廣泛地使用友誼戰！我們相信，隨時都可以練習進攻能幫助您發掘新的進攻和防守策略，也可以使部落成員之間的配合更加天衣無縫。
